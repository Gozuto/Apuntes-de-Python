name = "luis"

print(name) #imprime nombre
print(len(name)) #longitud
print(name.find("l")) #buscar letra
print(name.capitalize()) #Primer mayuscula
print(name.upper()) #TODO MAYUSCULAS
print(name.lower()) #todo minusculas
print(name.isdigit()) #es un numero?
print(name.isalpha()) #Solo letras? no incluye Espacios ni caracteres especiales
print(name.count("s")) #contar cuantas letras "x" hay (no cuenta mayusculas/minusculas segun se ordene)
print(name.replace("s","z")) #remplazar una letra
print(name*4) #imprimir varias veces
